import type { PageServerLoad } from './$types';

export const load: PageServerLoad = async ({ parent }) => {
  // On récupère les données du layout (user, rooms, etc.)
  const { user } = await parent();

  // Prépare juste ce dont ta page a besoin
  const userSettings = {
    name: user?.fullname ?? '',
    email: user?.email ?? '',
  };

  return { userSettings };
};